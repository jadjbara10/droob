// Governorate name → code mapping
const GOV_CODE: Record<string, string> = {
  "اربد": "IR",
  "البلقاء": "BA",
  "الزرقاء": "ZA",
  "الطفيلة": "TA",
  "الكرك": "KA",
  "المفرق": "MF",
  "جرش": "JE",
  "عجلون": "AJ",
  "مادبا": "MD",
  "معان": "MN",
  "العاصمة": "AM",
};

interface Terminal {
  governorate_ar: string;
  name_ar: string;
  code: string;
  lat: number;
  lng: number;
  is_terminal: boolean;
  is_landmark: boolean;
}

function normalize(str: string): string {
  return str.replace(/[\u064B-\u065F\u0670\u06D6-\u06ED]/g, "").trim();
}

function parseExcel(): Terminal[] {
  // Read the xlsx file using a simple approach - parse raw data
  // Since we can't rely on npm modules being installed, we'll use the data directly
  const rawData = fs.readFileSync(path.resolve(__dirname, "../../scripts/src/excel-raw-data.json"), "utf-8");
  const rows: string[][] = JSON.parse(rawData);
  
  const terminals: Terminal[] = [];
  let seq = 1;

  for (const row of rows) {
    const gov = normalize(row[0] || "");
    const name = normalize(row[1] || "");
    if (!gov || !name) continue;

    const govCode = GOV_CODE[gov] || "XX";
    const code = `TM-${govCode}-${String(seq).padStart(3, "0")}`;
    
    const coords = KNOWN_COORDS[name];
    if (!coords) {
      console.warn(`⚠️  No coordinates for: ${name} (${gov})`);
      continue;
    }

    terminals.push({
      governorate_ar: gov,
      name_ar: name,
      code,
      lat: coords.lat,
      lng: coords.lng,
      is_terminal: true,
      is_landmark: true,
    });
    seq++;
  }

  return terminals;
}

function generateSeedScript(terminals: Terminal[]): string {
  return `/**
 * دروب — Seed: Jordan Transit Terminals (from المجمعات_بالاردن.xlsx)
 * Auto-generated by extract-excel-stops.ts
 * Usage: npx tsx drizzle/seed-terminals.ts
 */
import "dotenv/config";
import { drizzle } from "drizzle-orm/postgres-js";
import postgres from "postgres";
import * as schema from "./schema.js";
import { eq } from "drizzle-orm";

if (!process.env.DATABASE_URL) {
  console.error("❌ DATABASE_URL is required in .env file");
  process.exit(1);
}

const sql = postgres(process.env.DATABASE_URL, { max: 1 });
const db = drizzle(sql, { schema });

const terminalsData = ${JSON.stringify(terminals.map(t => ({
  code: t.code,
  name_ar: t.name_ar,
  name_en: t.name_ar, // Will be transliterated separately
  lat: t.lat,
  lng: t.lng,
  governorate: t.governorate_ar,
  is_terminal: true,
  is_landmark: true,
  has_shelter: true,
  has_lighting: true,
})), null, 2)};

function stableUUID(seed: string): string {
  let hash = 0;
  for (let i = 0; i < seed.length; i++) {
    const char = seed.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash;
  }
  const hex = Math.abs(hash).toString(16).padStart(32, "0").slice(0, 32);
  return \`\${hex.slice(0,8)}-\${hex.slice(8,12)}-\${hex.slice(12,16)}-\${hex.slice(16,20)}-\${hex.slice(20,32)}\`;
}

async function seedTerminals() {
  console.log("🚏 زراعة محطات المجمعات من ملف Excel...\\n");
  
  let inserted = 0;
  let skipped = 0;

  for (const t of terminalsData) {
    const [existing] = await db.select()
      .from(schema.stops)
      .where(eq(schema.stops.code, t.code))
      .limit(1);
    
    if (existing) {
      console.log(\`  ⏭ \${t.name_ar} (\${t.code}) - موجودة مسبقاً\`);
      skipped++;
      continue;
    }

    await db.insert(schema.stops).values({
      id: stableUUID(\`stop-\${t.code}\`),
      ...t,
    });
    console.log(\`  ✓ \${t.name_ar} (\${t.code})\`);
    inserted++;
  }

  console.log(\`\\n✅ تمت زراعة المحطات: \${inserted} جديدة, \${skipped} موجودة مسبقاً\`);
  console.log(\`📍 الإجمالي: \${terminalsData.length} محطة\`);
}

seedTerminals()
  .then(() => process.exit(0))
  .catch((err) => {
    console.error("❌ فشلت الزراعة:", err);
    process.exit(1);
  });
`;
}

// ─── Main ───
async function main() {
  console.log("📊 استخراج بيانات المجمعات من Excel...\n");
  
  // We'll read the Excel data from a pre-prepared JSON extract
  // (the xlsx file has 37 actual data rows + 4 empty)
  const terminals = parseExcel();
  
  console.log(`✅ تم استخراج ${terminals.length} محطة\n`);

  // 1. Write JSON
  fs.mkdirSync(path.dirname(OUTPUT_JSON), { recursive: true });
  fs.writeFileSync(OUTPUT_JSON, JSON.stringify(terminals, null, 2), "utf-8");
  console.log(`📄 JSON saved: ${OUTPUT_JSON}`);

  // 2. Write seed script
  const seedContent = generateSeedScript(terminals);
  fs.writeFileSync(OUTPUT_SEED, seedContent, "utf-8");
  console.log(`🌱 Seed script: ${OUTPUT_SEED}`);

  console.log("\n📋 Terminals summary:");
  const byGov = new Map<string, Terminal[]>();
  for (const t of terminals) {
    const list = byGov.get(t.governorate_ar) || [];
    list.push(t);
    byGov.set(t.governorate_ar, list);
  }
  for (const [gov, list] of byGov) {
    console.log(`  ${gov}: ${list.length} مجمعات`);
  }
}

main().catch(console.error);